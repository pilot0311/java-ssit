package days01;

public class Ex05 {

	public static void main(String[] args) {
		
		
	
		
		//+ 덧셈 연산자
		System.out.println(3+5); //8
		//+ 문자열 연결 연산자
		System.out.println("이름: "+"신기범"); //이름: 신기범
		String name = "신기범";
		System.out.println("이름: "+ name); //이름: 신기범
		//"문자열" + 정수
		//정수 + "문자열"
		System.out.println("이름: " + 20); //"이름: 20"
		int age = 25;
		System.out.println("이름: " + age); //"이름: 25"
		
	}// main
}// class